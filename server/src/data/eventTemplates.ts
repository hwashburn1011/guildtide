/** T-0861: Enhanced event data schema */
export type EventRarity = 'common' | 'uncommon' | 'rare' | 'legendary';
export type EventCategory =
  | 'weather'
  | 'economy'
  | 'military'
  | 'exploration'
  | 'social'
  | 'magical'
  | 'seasonal'
  | 'crisis'
  | 'opportunity';

export interface EventTemplate {
  id: string;
  title: string;
  description: string;
  category: EventCategory;
  rarity: EventRarity;
  /** Illustration placeholder key for client-side art */
  illustration?: string;
  trigger: {
    weather?: string[];
    minFloodRisk?: number;
    minEssence?: number;
    season?: string[];
    chance: number;
    /** Minimum guild level required */
    minGuildLevel?: number;
    /** Cooldown in hours before this event can fire again */
    cooldownHours?: number;
  };
  durationHours: number;
  choices: EventChoice[];
  /** If set, resolving this event can trigger a chain follow-up */
  chainId?: string;
  chainStep?: number;
}

export interface EventChoice {
  label: string;
  description: string;
  requires?: {
    heroRole?: string;
    heroCount?: number;
    resource?: string;
    amount?: number;
    research?: string;
  };
  risk: number;
  rewards: {
    resources?: Record<string, number>;
    xp?: number;
    items?: string[];
    narrative: string;
  };
  failNarrative: string;
  /** Next chain step triggered on success (for branching narratives) */
  nextChainStep?: number;
}

